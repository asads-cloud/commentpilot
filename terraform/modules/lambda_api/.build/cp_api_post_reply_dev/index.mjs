var ISSUER = process.env.COGNITO_ISSUER;
var AUDIENCE = process.env.COGNITO_AUDIENCE;
if (!ISSUER) throw new Error("Missing required env var: COGNITO_ISSUER");
if (!AUDIENCE) throw new Error("Missing required env var: COGNITO_AUDIENCE");
var ISS = ISSUER.replace(/\/+$/, "");
var JWKS = createRemoteJWKSet(new URL(`${ISS}/.well-known/jwks.json`), {
  cooldownDuration: 3e4
  // 30s
});
var CLOCK_TOLERANCE_SEC = 60;
var AuthError = class extends Error {
  status = 401;
  constructor(message2) {
    super(message2);
    this.name = "AuthError";
  }
};
function mapJoseError(e) {
  if (e instanceof errors_exports.JWTExpired) return new AuthError("JWT expired");
  if (e instanceof errors_exports.JWTInvalid) return new AuthError("Invalid JWT");
  if (e instanceof errors_exports.JWSSignatureVerificationFailed)
    return new AuthError("Signature verification failed");
  if (e instanceof errors_exports.JWSInvalid) return new AuthError("Invalid JWS");
  if (e instanceof errors_exports.JOSEError) return new AuthError(e.message);
  return new AuthError(e?.message ?? "Unauthorized");
}
async function verifyJwt(authHeader) {
  if (!authHeader?.startsWith("Bearer ")) {
    throw new AuthError("Missing or malformed Authorization header");
  }
  const token = authHeader.slice("Bearer ".length);
  try {
    const { payload } = await jwtVerify(token, JWKS, {
      algorithms: ["RS256"],
      issuer: ISS,
      audience: AUDIENCE,
      clockTolerance: CLOCK_TOLERANCE_SEC
    });
    if (payload.iss !== ISS) {
      console.warn("Issuer mismatch (post-verify)", { tokenIss: payload.iss, expected: ISS });
    }
    if (payload.aud !== AUDIENCE) {
      console.warn("Audience mismatch (post-verify)", { tokenAud: payload.aud, expected: AUDIENCE });
    }
    return payload;
  } catch (e) {
    console.error("JWT verification failed", {
      message: e?.message,
      name: e?.name
    });
    throw mapJoseError(e);
  }
}

